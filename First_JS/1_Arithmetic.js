/**
 * Toán tử số học
    +               --> Cộng
    -               --> Trừ
    *               --> Nhân
    /               --> Chia
    **              --> Luỹ thừa
    %               --> Chia lấy phần dư
    ++              --> Tăng 1 giá trị số
    --              --> Giảm 1 giá trị số
 */


var a = 10;
var b = 2;

var c = a % b;

console.log(c);

a++;
console.log(a);


